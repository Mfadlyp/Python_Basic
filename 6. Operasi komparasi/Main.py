# operasi komparasi
# setiap hasil dari komparasi adalah boolean

# >, <, =, <=, >=, ==, !=, is, is not

a = 10
b = 5

# operasi >
hasil = a > b
print(a,">",b,"=",hasil)

# operasi <
hasil = a < b
print(a,"<",b,"=",hasil)

# operasi =
hasil = a == b
print(a,"==",b,"=",hasil)

# operasi <=
hasil = a <= b
print(a,"<=",b,"=",hasil)

# operasi >=
hasil = a >= b
print(a,">=",b,"=",hasil)

# operasi !=
hasil = a != b
print(a,"!=",b,"=",hasil)

# operasi is
hasil = a is b
print(a,"is",b,"=",hasil)

# operasi is not
hasil = a is not b
print(a,"is not",b,"=",hasil)