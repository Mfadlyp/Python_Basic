# operasi aritmatika

a = 10
b = 4
# operasi pertambahan
hasil = a + b
print(a,"+",b, "=",hasil)

# operasi pengurangan
hasil = a - b
print(a,"-",b, "=",hasil)

# operasi perkalian
hasil = a * b
print(a,"*",b, "=",hasil)

# operasi pembagian
hasil = a / b
print(a,"/",b, "=",hasil)

# operasi pangkat/eksponen
hasil = a ** b
print(a,"**",b, "=",hasil)

# operasi modulus, sisa bagi
hasil = a % b
print(a,"%",b,"=",hasil)

# operasi floar division, pembulatan kebalikan dari modulus
hasil = a // b
print(a,"//",b,"=",hasil)

# prioritas operasi python
# 1. ()
# 2. ** 
# 3. *, /, %, //, 
# 4. pertambahan
# 5. pengurangan
