# operasi yg dapat dilakukan penyingkatan
# operasi ditambah dengan assigment

a = 10 # ini adalah assigmnet

a += 1 # sama dengan a = a +  1
a -= 1 # sama dengan a = a - 1
a *= 1 # sama dengan a = a * 1
a /= 1 # sama dengan a = a / 1
a %= 1 # sama dengan a = a % 1
a **= 1 # sama dengan a = a ** 1
a //= 1 # sama dengan a = a // 1

print(a)